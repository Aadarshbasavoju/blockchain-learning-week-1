// Using let keyword
let name = "Aadarsh";
let age = 21;
let city = "Hyderabad";

console.log("Name:", name);
console.log("Age:", age);
console.log("City:", city);

// Using const keyword
const myName = "Aadarsh";
const myAge = 21;
const myCity = "Hyderabad";

console.log("Name:", myName);
console.log("Age:", myAge);
console.log("City:", myCity);

// Using var keyword
var userName = "Aadarsh";
var userAge = 21;
var userCity = "Hyderabad";

console.log("Name:", userName);
console.log("Age:", userAge);
console.log("City:", userCity);
